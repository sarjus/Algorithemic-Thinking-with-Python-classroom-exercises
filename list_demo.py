'''
Objective: How to create lists, access elements using indexing, and modify list elements.
Instructions:
Create a list called fruits with the following elements: "apple", "banana", "cherry", "date".
Print the second element in the list.
Modify the last element of the list to "dragonfruit".Print the modified list

Author: Sarju S
'''
# Step 1: Creating the list
fruits = ["apple", "banana", "cherry", "date"]

# Step 2: Accessing the second element
print(fruits[1])  # Output: banana

# Step 3: Modifying the last element
fruits[3] = "dragonfruit"

# Step 4: Printing the modified list
print(fruits)  # Output: ["apple", "banana", "cherry", "dragonfruit"]
