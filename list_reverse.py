# Step 1: Creating the list of numbers
numbers = [10, 20, 30, 40, 50]

# Step 2: Reversing the list
numbers.reverse()

# Step 3: Printing the reversed list
print(numbers)  # Output: [50, 40, 30, 20, 10]
